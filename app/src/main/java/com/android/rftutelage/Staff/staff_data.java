package com.android.rftutelage.Staff;

public class staff_data {

    String faculty_name;
    String faculty_image;
    String post;
    String qualification;
    String courses_taught;
    String area_of_expertise;
    String Academic_experience;
    String email;
    String phone_no;

    public String getFaculty_name() {
        return faculty_name;
    }

    public void setFaculty_name(String faculty_name) {
        this.faculty_name = faculty_name;
    }

    public String getFaculty_image() {
        return faculty_image;
    }

    public void setFaculty_image(String faculty_image) {
        this.faculty_image = faculty_image;
    }

    public String getPost() {
        return post;
    }

    public void setPost(String post) {
        this.post = post;
    }

    public String getQualification() {
        return qualification;
    }

    public void setQualification(String qualification) {
        this.qualification = qualification;
    }

    public String getCourses_taught() {
        return courses_taught;
    }

    public void setCourses_taught(String courses_taught) {
        this.courses_taught = courses_taught;
    }

    public String getArea_of_expertise() {
        return area_of_expertise;
    }

    public void setArea_of_expertise(String area_of_expertise) {
        this.area_of_expertise = area_of_expertise;
    }

    public String getAcademic_experience() {
        return Academic_experience;
    }

    public void setAcademic_experience(String academic_experience) {
        Academic_experience = academic_experience;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone_no() {
        return phone_no;
    }

    public void setPhone_no(String phone_no) {
        this.phone_no = phone_no;
    }
}
